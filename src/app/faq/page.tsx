export default function FAQ() {
  const [openIndex, setOpenIndex] = useState<number | null>(null)

  const playClickSound = () => {
    const audio = new Audio('/sounds/sao_click.mp3')
    audio.volume = 0.3
    audio.play().catch(() => {})
  }

  return (
    <div className="min-h-screen pt-24 pb-12 px-6">
      <Navbar />
      
      <div className="max-w-4xl mx-auto">
        <h1 className="text-4xl font-bold text-white mb-4">FAQ</h1>
        <p className="text-slate-400 mb-8">Часто задаваемые вопросы о OtakuZone</p>
        
        <div className="space-y-4">
          {faqItems.map((item, index) => (
            <Card key={index} hover>
              <motion.button
                onClick={() => {
                  setOpenIndex(openIndex === index ? null : index)
                  playClickSound()
                }}
                className="w-full text-left"
              >
                <div className="flex justify-between items-center">
                  <h3 className="text-lg text-white font-semibold">{item.question}</h3>
                  <motion.div
                    animate={{ rotate: openIndex === index ? 180 : 0 }}
                    transition={{ duration: 0.2 }}
                    className="text-primary-500"
                  >
                    ▼
                  </motion.div>
                </div>
              </motion.button>
              
              <AnimatePresence>
                {openIndex === index && (
                  <motion.div
                    initial={{ opacity: 0, height: 0 }}
                    animate={{ opacity: 1, height: 'auto' }}
                    exit={{ opacity: 0, height: 0 }}
                    transition={{ duration: 0.2 }}
                    className="pt-4"
                  >
                    <p className="text-slate-300">{item.answer}</p>
                  </motion.div>
                )}
              </AnimatePresence>
            </Card>
          ))}
        </div>
      </div>
    </div>
  )
}
